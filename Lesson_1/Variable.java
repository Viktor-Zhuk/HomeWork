public class Variable {
    
    public static void main(String[] args) {
        byte numberOfCores = 6;
        short powerUnit = 65;
        int graphicsFrequency = 350;
        long diskSpace = 1000000L;
        float clockFrequency = 3.50F;
        double operationalMemory = 16.0;
        char processorIndex = 'i';
        boolean isСamera = true;
        System.out.println("numberByte: " + numberOfCores);
        System.out.println("numberShort: " + powerUnit);
        System.out.println("numberInt: " + graphicsFrequency);
        System.out.println("numberLong: " + diskSpace);
        System.out.println("numberFloat: " + clockFrequency);
        System.out.println("numberDouble: " + operationalMemory);
        System.out.println("letter: " + processorIndex);
        System.out.println("isTrue: " + isСamera);
    }
}
